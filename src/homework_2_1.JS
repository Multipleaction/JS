'number' + 23 + 32; // number2332
// Конкатенація рядків: перше значення 'number' об'єднується з числом 23, а потім результат конкатенації об'єднується з числом 32

41 + 1 + 'number'; // 42number
// Додавання чисел: числа 41 та 1 додаються, а результат конкатенується з стрінгою 'number'

null + 1; // 1
// Конвертація null у число 0, а потім додається 1

'five' + +'two'; // fiveNaN
// Оператор + намагається конвертувати рядок 'two' у число
// (це не вдається, тому повертає NaN), результат об'єднується з стрінгою 'five'

2 && 7; // 7
// У виразі 2 && 7 оператор && виконує логічне І між двома операндами 2 та 7.
// Jбидва операнди є істинними  тому результатом буде значення другого операнду

+'40' + +'2'; // 42
// Рядок '40' конвертується в число 40 за допомогою унарого опретора +
// Ряжок '2' конвертується число 2 за допомогою унарного оператора +
// 40 + 2 = 42

'10' - 5 === 6; // false
// Рядок '10' конвертується у число, від якого потім віднімається 5. Результат порівнюється з числом 6
// 6 не дорівню 5 тому поверається false

true + false; // 1
// Значення true конвертується у 1, а false у 0, після чого вони додаються
// 0 + 1 = 1

'4px' - 3; // NaN
// Рядок '4px' не може бути конвертований в число, тому результат буде NaN

'4' - 3; // 1
// Рядок '4' конвертується в число, після чого від числа віднімається 3
// 4 -3 = 1

'2' + 3 ** 2; // 29
// Спочатку виконується приведення в ступніь 3 ** 2
// Потім резульат конкатенується з рядком '2'

12 / '6'; // 2
// Рядок '6' конвертується в число, після чого виконується ділення

23 + 42 + 'number'; // 65number
// Спочатку числа додаються, а потім результат конкатенується з рядком 'number'

'10' + (5 === 6); // 10false
//  (5 === 6) дорівнює false, результат конвертується у рядок 'false' та потім об'єднується з рядком '10'

'number' + 15 + 3; // number153
// Рядок 'number' об'єднується з числом 15, а потім результат конкатенується з числом 3.

